package darks.orm.exceptions;

public class JsAspectException extends AspectException
{
    
    private static final long serialVersionUID = 8915867184036682067L;
    
    public JsAspectException()
    {
        super();
    }
    
    public JsAspectException(String message, Throwable cause)
    {
        super(message, cause);
    }
    
    public JsAspectException(String message)
    {
        super(message);
    }
    
    public JsAspectException(Throwable cause)
    {
        super(cause);
    }
    
}
